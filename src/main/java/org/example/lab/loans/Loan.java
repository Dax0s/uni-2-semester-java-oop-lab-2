package org.example.lab.loans;

public class Loan {
    private float sum;
    private int years;
    private int months;
    private float yearlyPercentage;

    public Loan(float sum, int years, int months, float yearlyPercentage) {
        this.sum = sum;
        this.years = years;
        this.months = months;
        this.yearlyPercentage = yearlyPercentage;
    }


}
